    public boolean checkRong() {
        if (txtFullName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Full name không được để trống!");
            txtFullName.requestFocus();
            return false;
        }
        if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Email không được để trống!");
            txtEmail.requestFocus();
            return false;
        }
        if (txtMatKhauHT.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mật khẩu hiện tại không được để trống!");
            txtMatKhauHT.requestFocus();
            return false;
        }
        if (txtMatKhauNow.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mật khẩu mới không được để trống!");
            txtMatKhauNow.requestFocus();
            return false;
        }
        return true;
    }
    
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if (!checkRong()) {
            return;
        }
        String fullName = txtFullName.getText();
        String email = txtEmail.getText();
        String matKhauHT = new String(txtMatKhauHT.getPassword());
        String matKhauNow = new String(txtMatKhauNow.getPassword());

        if (!accountsDAO.checkEmail(email)) {
            JOptionPane.showMessageDialog(rootPane, "Email không hợp lệ!");
            return;
        }
        if (!accountsDAO.checkCurrentPassword(id, matKhauHT)) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu hiện tại không đúng!");
            return;
        }
        if (!accountsDAO.checkPassword(matKhauNow)) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu mới không hợp lệ!");
            return;
        }
        // Hash the new password
        String hashedPassword = BCrypt.hashpw(matKhauNow, BCrypt.gensalt());
        try {
            if (accountsDAO.updateAccount(id, fullName, email, hashedPassword)) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin thành công!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin thất bại!");
            }
        } catch (Exception e) {
            java.util.logging.Logger.getLogger(UpdateThongTin.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
        }
        
    }//GEN-LAST:event_btnLuuActionPerformed
    
